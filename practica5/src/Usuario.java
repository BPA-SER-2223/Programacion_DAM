public class Usuario {
    private String name;
    private String second_name;
    private Integer postal_code;
    private String address;
    private String email;
    private Integer password;
    public Usuario(String n,String s,Integer c,String a,String e,Integer p){
        this.name=n;
        this.second_name=s;
        this.postal_code=c;
        this.address=a;
        this.email=e;
        this.password=p;
    }


}
