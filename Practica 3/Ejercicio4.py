num1=int(input("Indica el primer número\n"))#Inicializamos una variable de tipo número entero
num2=int(input("Indica el segundo número\n"))#Inicializamos una variable de tipo número entero

print("Suma: "+str(num1+num2))#Mensaje con operación (Suma de los valores)
print("Resta: "+str(num1-num2))#Mensaje con operación (Resta de los valores)
print("División: "+str(num1/num2))#Mensaje con operación (División de los valores)
print("Multiplicación: "+str(num1*num2))#Mensaje con operación (Multiplicación de los valores)