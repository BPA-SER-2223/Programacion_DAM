public class Persona {
    private String dni;
    private String nombre;
    private Integer edad;
    private String sexo;

    public Persona(String dni_p,String nombre_p,Integer edad_p,String sexo_p) {
        this.dni = dni_p;
        this.nombre = nombre_p;
        this.edad = edad_p;
        this.sexo = sexo_p;
    }
}
